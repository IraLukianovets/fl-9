export function addition(n1, n2) {
    return parseFloat(n1) + parseFloat(n2)
}

export function subtraction(n1, n2) {
    return parseFloat(n1) - parseFloat(n2)
}

export function multiplication(n1, n2) {
    return parseFloat(n1) * parseFloat(n2)
}

export function division (n1, n2) {
    return parseFloat(n1) / parseFloat(n2)
}